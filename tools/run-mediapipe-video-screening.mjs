import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "playwright";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const outputDir = path.join(rootDir, "docs");
const baseUrl = process.env.PARTY_FACE_AR_URL || "http://127.0.0.1:8000";

const samples = [
  "friends-selfie-pool-mixkit-43089.mp4",
  "birthday-party-hats-mixkit-4608.mp4",
  "friends-group-selfie-mixkit-45657.mp4",
  "friends-smiling-camera-mixkit-5891.mp4",
  "friends-window-selfie-mixkit-8780.mp4",
  "nightclub-two-girls-mixkit-42292.mp4",
  "pexels-three-friends-selfie-35050402.mp4",
  "pexels-group-friends-5708805.mp4",
  "pexels-group-selfie-6214756.mp4",
  "pexels-group-selfie-crop-6214756.mp4",
  "pexels-friends-party-5529160.mp4",
  "party-hats-crop-wide_faces.webm"
];

const seconds = Number(process.env.MP_SCREEN_SECONDS || 8);
const sampleFps = Number(process.env.MP_SCREEN_FPS || 4);

await fs.mkdir(outputDir, { recursive: true });

const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({
  viewport: { width: 1280, height: 900 }
});

page.setDefaultTimeout(90000);
page.on("console", (message) => {
  if (message.type() === "error") {
    console.error(`[browser:${message.type()}] ${message.text()}`);
  }
});
page.on("pageerror", (error) => {
  console.error(`[browser:pageerror] ${error.message}`);
});

const results = [];

try {
  for (const sample of samples) {
    const videoUrl = `./samples/${sample}`;
    const verifyUrl = new URL("/mediapipe-verify.html", baseUrl);
    verifyUrl.searchParams.set("video", videoUrl);
    verifyUrl.searchParams.set("seconds", String(seconds));
    verifyUrl.searchParams.set("sampleFps", String(sampleFps));
    verifyUrl.searchParams.set("autorun", "1");

    console.log(`Screening ${sample}...`);
    await page.goto(verifyUrl.toString(), { waitUntil: "domcontentloaded" });
    await page.waitForFunction(() => window.__MEDIAPIPE_VERIFY__?.ready === true, null, {
      timeout: 90000
    });
    await page.waitForFunction(() => window.__MEDIAPIPE_VERIFY__?.running === false && window.__MEDIAPIPE_VERIFY__?.result, null, {
      timeout: 180000
    });

    const result = await page.evaluate(() => window.__MEDIAPIPE_VERIFY__.result);
    results.push({
      sample,
      ...result
    });
    console.log(
      `${sample}: max=${result.maxFaces ?? "ERR"} avg=${result.averageFaces ?? "-"} 3+=${formatPercent(result.threePlusRatio ?? 0)}`
    );
  }
} finally {
  await browser.close();
}

const sorted = [...results].sort((a, b) => {
  const byMax = (b.maxFaces ?? -1) - (a.maxFaces ?? -1);
  if (byMax) {
    return byMax;
  }
  return (b.threePlusRatio ?? 0) - (a.threePlusRatio ?? 0);
});

const jsonPath = path.join(outputDir, "mediapipe-video-screening.json");
await fs.writeFile(jsonPath, JSON.stringify({ baseUrl, seconds, sampleFps, results: sorted }, null, 2));

const markdownPath = path.join(outputDir, "mediapipe-video-screening.md");
await fs.writeFile(markdownPath, buildMarkdown(sorted));

console.log(`Wrote ${path.relative(rootDir, jsonPath)}`);
console.log(`Wrote ${path.relative(rootDir, markdownPath)}`);

function buildMarkdown(rows) {
  const table = rows
    .map((row) => {
      if (row.error) {
        return `| ${row.sample} | error | - | - | - | ${row.error.replace(/\|/g, "\\|")} |`;
      }
      return `| ${row.sample} | ${row.maxFaces} | ${row.averageFaces} | ${formatPercent(row.twoPlusRatio)} | ${formatPercent(row.threePlusRatio)} | ${formatHistogram(row.countHistogram)} |`;
    })
    .join("\n");

  const best = rows.find((row) => !row.error);
  const recommendation = best?.maxFaces >= 3
    ? `MediaPipe detected at least 3 simultaneous faces in \`${best.sample}\`, so migration is worth pursuing.`
    : "No screened sample reached 3 simultaneous MediaPipe detections yet; keep MediaPipe as the likely base, but obtain or record a stronger 3-person front-facing test clip before full migration.";

  return `# MediaPipe Video Screening

Generated by \`tools/run-mediapipe-video-screening.mjs\`.

- Sample duration: ${seconds}s
- Sample rate: ${sampleFps} FPS
- Model: MediaPipe Face Landmarker via \`@mediapipe/tasks-vision\`
- Config: \`numFaces: 4\`, blendshapes enabled, facial transformation matrices enabled

${recommendation}

| Sample | Max faces | Avg faces | 2+ frames | 3+ frames | Histogram |
| --- | ---: | ---: | ---: | ---: | --- |
${table}
`;
}

function formatPercent(value) {
  return `${Math.round(value * 100)}%`;
}

function formatHistogram(histogram = {}) {
  return Object.entries(histogram)
    .sort(([a], [b]) => Number(a) - Number(b))
    .map(([count, frames]) => `${count}:${frames}`)
    .join(", ");
}
